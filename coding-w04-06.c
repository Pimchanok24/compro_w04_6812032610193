#include <stdio.h>
const int GLOBAL_RATE = 20;
void calculate() {
    const int LOCAL_BONUS = 80;
    printf("GLOBAL_RATE in calculate() = %d\n", GLOBAL_RATE);
    printf("LOCAL_BONUS in calculate() = %d\n", LOCAL_BONUS);
}
int main() {
    calculate();
    printf("GLOBAL_RATE in main() = %d\n", GLOBAL_RATE); 
    return 0;
}
/*/*1. ค่าคงที่ GLOBAL_RATE และ LOCAL_BONUS ต่างกันอย่างไร
ค่าคงที่ GLOBAL_RATE และ LOCAL_BONUS มีความแตกต่างกันที่ ตำแหน่งการประกาศและขอบเขตการใช้งาน (Scope)
-GLOBAL_RATE เป็นค่าคงที่ที่ประกาศนอกฟังก์ชัน (Global constant) จึงสามารถใช้งานได้จากทุกฟังก์ชันในโปรแกรม
-LOCAL_BONUS เป็นค่าคงที่ที่ประกาศภายในฟังก์ชัน calculate() (Local constant) ทำให้สามารถใช้งานได้เฉพาะภายในฟังก์ชันนั้นเท่านั้น
2. เกิดอะไรขึ้นถ้าเราเขียน GLOBAL_RATE = 20; หรือ LOCAL_BONUS = 80;?
หากมีการเขียน GLOBAL_RATE = 20; หรือ LOCAL_BONUS = 80; ในโปรแกรม จะทำให้เกิด ข้อผิดพลาดในการคอมไพล์ (Compile Error)
เนื่องจากค่าทั้งสองนี้ถูกกำหนดเป็น const ซึ่งหมายความว่าเป็นค่าคงที่ ไม่สามารถเปลี่ยนแปลงค่าได้หลังจากประกาศ
3. ทำไม LOCAL_BONUS ถึงไม่สามารถเรียกใช้ใน main() ได้?
เพราะ LOCAL_BONUS เป็นตัวแปรภายในฟังก์ชัน (local variable) ที่ถูกประกาศไว้ในฟังก์ชัน calculate() เท่านั้น
ดังนั้นจึงไม่สามารถใช้งานหรือเข้าถึงได้จากภายนอกฟังก์ชัน เช่นใน main() ซึ่งอยู่นอกขอบเขตของตัวแปรนี้
หากพยายามใช้งาน LOCAL_BONUS ใน main() โปรแกรมจะเกิดข้อผิดพลาดในการคอมไพล์
4. อธิบายและสรุปผลที่ได้จากการทดลองสิ่งที่ได้เรียนรู้
จากการทดลองในครั้งนี้ ทำให้ได้เรียนรู้ว่า:
-ค่าคงที่ (const) ไม่สามารถเปลี่ยนค่าได้หลังจากประกาศ
-ตัวแปรที่ประกาศแบบ Global สามารถเรียกใช้งานได้จากทุกฟังก์ชันในโปรแกรม
-ตัวแปรที่ประกาศแบบ Local สามารถใช้งานได้เฉพาะภายในฟังก์ชันที่ประกาศเท่านั้น
-การเข้าใจเรื่องขอบเขตของตัวแปร (Variable Scope) เป็นสิ่งสำคัญในการเขียนโปรแกรม
-ทำให้สามารถเขียนโปรแกรมที่มีโครงสร้างชัดเจน และจัดการกับตัวแปรได้อย่างเหมาะสม */